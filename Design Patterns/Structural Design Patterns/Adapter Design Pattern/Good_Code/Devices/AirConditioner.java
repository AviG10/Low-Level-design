package Good_Code.Devices;

public class AirConditioner {
    public void connectViaBluetooth(){
        System.out.println("Air Conditioner Connect via Bluetooth");
    }

    public void disconnectViaBluetooth(){
        System.out.println("Air Conditioner disconnected from Bluetooth");
    }

    public void startCooling(){
        System.out.println("Air Conditioner start cooling");
    }

    public void stopCooling(){
        System.out.println("Air Conditioner stop cooling");
    }
}
