package Good_Code.Devices;

public class SmartLight {
    public void connectToWifi(){
        System.out.println("SmartLight Connecting to wifi");
    }

    public void disconnectFromWifi(){
        System.out.println("SmartLight Disconnecting from wifi");
    }

    public void switchOn(){
        System.out.println("SmartLight Switching on");
    }

    public void switchOff(){
        System.out.println("SmartLight Switching off");
    }
}
